using System;

namespace PR2_Alumnos
{
    class Alumno
    {
       
        string nombre;
        string apellido;
        int edad;
        int matricula;

        public Alumno(string nombre, string apellido, int edad, int matricula)
        {

            this.nombre = nombre;

            this.apellido = apellido;

            this.edad = edad;

            this.matricula = matricula;

        }













    }
}
